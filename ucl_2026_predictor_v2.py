def sim_match(t1,t2,prob_dict,upset=0.18):
    p1,p2 = prob_dict.get(t1,5),prob_dict.get(t2,5)
    noise = random.gauss(0,upset*(p1+p2))
    p1a,p2a = max(0.05,p1+noise),max(0.05,p2-noise)
    return t1 if random.random()<p1a/(p1a+p2a) else t2

def sim_tournament(bracket,prob_dict):
    r16 = [sim_match(a,b,prob_dict) for a,b in bracket]
    qf  = [sim_match(r16[i],r16[i+1],prob_dict) for i in range(0,8,2)]
    sf  = [sim_match(qf[0],qf[1],prob_dict), sim_match(qf[2],qf[3],prob_dict)]
    return sim_match(sf[0],sf[1],prob_dict)

N=15000; random.seed(42); np.random.seed(42); counts={}
for _ in range(N):
    c=sim_tournament(BRACKET,prob_dict); counts[c]=counts.get(c,0)+1

mc_df = pd.DataFrame([{'team':t,'count':c,'pct':round(c/N*100,2)} for t,c in sorted(counts.items(),key=lambda x:-x[1])])
