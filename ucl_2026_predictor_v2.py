def bw(t1,t2):
    p1,p2=prob_dict.get(t1,5),prob_dict.get(t2,5)
    w=t1 if p1>=p2 else t2; l=t2 if p1>=p2 else t1
    return w,l,max(p1,p2)/(p1+p2)*100

def pm(t1,t2,label=''):
    w,l,c=bw(t1,t2)
    print(f'   {t1:<22} vs {t2:<22} → ✅ {w} ({c:.0f}%)')
    return w

